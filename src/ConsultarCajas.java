    void cargar(String valor){
        String []  titulos ={"NOMBRE DE LA CARPETA", "FOLIOS", "NÂ° CAJA"};

        String[] registros = new String[3];
        
        String sql = "SELECT nomg,hojas,numcaja FROM carpetasg where nomg LIKE '%"+valor+"%'";
        
        model = new DefaultTableModel(null,titulos);
        
        
        
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                registros[0]=rs.getString("nomg");
                registros[1]=rs.getString("hojas");
                registros[2]=rs.getString("numcaja");
                model.addRow(registros);
            }
            
            t_datos.setModel(model);
            jlcanti.setText("Cantidad de Carpetas: "+t_datos.getRowCount());
            
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null,ex);
        }
        
        
        
    }
    
    
    

